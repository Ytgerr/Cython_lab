# ---------------------------------------------------------------------------
# Timing helper
# ---------------------------------------------------------------------------

def measure(fn: Callable, *args, repeats: int = 5) -> Tuple[float, int]:
    """
    Returns (best_wall_seconds, peak_memory_bytes).
    Memory is measured on the *first* call only (tracemalloc overhead is high).
    """
    # Memory — first call
    tracemalloc.start()
    fn(*args)
    _, peak = tracemalloc.get_traced_memory()
    tracemalloc.stop()

    # Time — best of `repeats`
    best = float("inf")
    for _ in range(repeats):
        t0 = time.perf_counter()
        fn(*args)
        elapsed = time.perf_counter() - t0
        if elapsed < best:
            best = elapsed

    return best, peak


# ---------------------------------------------------------------------------
# Data preparation helpers
# ---------------------------------------------------------------------------

def make_list_matrix(n: int) -> List[List[float]]:
    rng = np.random.default_rng(42)
    return rng.random((n, n)).tolist()


def make_np_matrix(n: int) -> np.ndarray:
    rng = np.random.default_rng(42)
    return np.ascontiguousarray(rng.random((n, n)), dtype=np.float64)


# ---------------------------------------------------------------------------
# Benchmark suite
# ---------------------------------------------------------------------------

OPERATIONS = ["matmul", "matrix_add", "frobenius_norm"]


def run_benchmarks(sizes: List[int], repeats: int) -> List[Dict]:
    records = []

    for N in sizes:
        print(f"\n{'='*60}")
        print(f"  Matrix size: {N}x{N}  (repeats={repeats})")
        print(f"{'='*60}")

        A_list = make_list_matrix(N)
        B_list = make_list_matrix(N)
        A_np   = make_np_matrix(N)
        B_np   = make_np_matrix(N)

        # ----------------------------------------------------------------
        # Pure Python
        # ----------------------------------------------------------------
        impls = [("python", py_matmul, py_add, py_norm, A_list, B_list)]

        # ----------------------------------------------------------------
        # Cython untyped — still uses Python lists
        # ----------------------------------------------------------------
        if CY_UNTYPED_OK:
            impls.append(("cy_untyped", cy_u_matmul, cy_u_add, cy_u_norm,
                           A_list, B_list))

        # ----------------------------------------------------------------
        # Cython typed — uses NumPy arrays (typed memoryviews)
        # ----------------------------------------------------------------
        if CY_TYPED_OK:
            impls.append(("cy_typed", cy_t_matmul, cy_t_add, cy_t_norm,
                           A_np, B_np))

        # ----------------------------------------------------------------
        # C via ctypes — uses NumPy arrays
        # ----------------------------------------------------------------
        if C_LIB_OK:
            impls.append(("c_ctypes", c_matmul, c_add, c_norm,
                           A_np, B_np))

        for (name, fn_mm, fn_add, fn_norm, A, B) in impls:
            # Skip matmul for large sizes in pure Python (too slow)
            skip_matmul = (name == "python" and N > 256)

            # matmul
            if skip_matmul:
                t_mm, m_mm = float("nan"), 0
                print(f"  [{name:12s}] matmul        SKIPPED (N>{256})")
            else:
                t_mm, m_mm = measure(fn_mm, A, B, repeats=repeats)
                print(f"  [{name:12s}] matmul        {t_mm*1000:10.3f} ms  "
                      f"mem={m_mm/1024:.1f} KB")

            # matrix_add
            t_add, m_add = measure(fn_add, A, B, repeats=repeats)
            print(f"  [{name:12s}] matrix_add    {t_add*1000:10.3f} ms  "
                  f"mem={m_add/1024:.1f} KB")

            # frobenius_norm
            t_norm, m_norm = measure(fn_norm, A, repeats=repeats)
            print(f"  [{name:12s}] frobenius_norm{t_norm*1000:10.3f} ms  "
                  f"mem={m_norm/1024:.1f} KB")

            for op, t, m in [("matmul", t_mm, m_mm),
                              ("matrix_add", t_add, m_add),
                              ("frobenius_norm", t_norm, m_norm)]:
                records.append({
                    "impl": name,
                    "N": N,
                    "op": op,
                    "time_s": t,
                    "mem_bytes": m,
                })

    return records


# ---------------------------------------------------------------------------
# Save results
# ---------------------------------------------------------------------------

def save_results(records: List[Dict], out_dir: str) -> None:
    os.makedirs(out_dir, exist_ok=True)

    csv_path = os.path.join(out_dir, "bench_results.csv")
    json_path = os.path.join(out_dir, "bench_results.json")

    with open(csv_path, "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=["impl", "N", "op",
                                                "time_s", "mem_bytes"])
        writer.writeheader()
        writer.writerows(records)

    with open(json_path, "w") as f:
        json.dump(records, f, indent=2)

    print(f"\nResults saved to:\n  {csv_path}\n  {json_path}")


