"""
Benchmark runner — compares all four implementations:
  1. Pure Python (lists)
  2. Cython untyped (compiled but no static types)
  3. Cython typed   (static types + typed memoryviews + nogil)
  4. C via ctypes   (compiled C, called through ctypes)

Metrics collected per (implementation x matrix_size x operation):
  * wall-clock time  (timeit, best of N repeats)
  * peak RSS memory  (tracemalloc for Python objects;
                      note: C/Cython heap is NOT tracked by tracemalloc)

Usage:
    python benchmarks/bench_runner.py [--sizes 64 128 256] [--repeats 5]

Output:
    benchmarks/results/bench_results.csv
    benchmarks/results/bench_results.json
    (plots are generated by the Jupyter notebook)
"""

